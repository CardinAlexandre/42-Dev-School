int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb <= 1)
	{
		return (2);
	}
	i = 2;
	while (i < 46340 && i < nb - 1)
	{
		if (nb % i != 0)
		{
			i++;
		}
		else
		{
			return (ft_find_next_prime(nb + 1));
		}
	}
	return (nb);
}
